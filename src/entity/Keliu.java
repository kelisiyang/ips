package entity;

public class Keliu {
	private Integer kid;
	private Integer louceng;
	private Integer keliu;
	public Integer getKid() {
		return kid;
	}
	public void setKid(Integer kid) {
		this.kid = kid;
	}
	public Integer getLouceng() {
		return louceng;
	}
	public void setLouceng(Integer louceng) {
		this.louceng = louceng;
	}
	public Integer getKeliu() {
		return keliu;
	}
	public void setKeliu(Integer keliu) {
		this.keliu = keliu;
	}
}